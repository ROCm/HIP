  cmpVal1(f1, 3);
  f1 = (signed short)1 * f1;
  cmpVal1(f1, 3);
  f1 = f1 * (unsigned int)1;
  cmpVal1(f1, 3);
  f1 = (unsigned int)1 * f1;
