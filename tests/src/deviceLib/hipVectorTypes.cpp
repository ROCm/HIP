  assert(!f1 == false);
  f1.x = 3;
  f1 = f1 * (unsigned char)1;
  cmpVal1(f1, 3);
  f1 = (unsigned char)1 * f1;
  cmpVal1(f1, 3);
  f1 = f1 * (signed char)1;
