#define cmpVal1(in, exp) \
  if(in.x != exp) { \
    std::cout<<"Failed at: "<<__LINE__<<" in func: "<<__func__<<" expected output: "<<exp<<" but got: "<<in.x<<std::endl; \
    assert(-1); \
  } \

#define cmpVal2(in, exp) \
  if(in.x != exp || in.y != exp) { \
