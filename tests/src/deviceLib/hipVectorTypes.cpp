  f1 ^= f2;
  cmpVal1(f1, 0);
  f1.x = 1;
  f1 <<= f2;
  cmpVal1(f1, 2);
  f1 >>= f2;
  cmpVal1(f1, 1);

  f1.x = 2;
  f2 = f1++;
  cmpVal1(f1, 3);
  cmpVal1(f2, 2);
  f2 = f1--;
  cmpVal1(f2, 3);
  cmpVal1(f1, 2);
  f2 = ++f1;
  cmpVal1(f1, 3);
  cmpVal1(f2, 3);
  f2 = --f1;
